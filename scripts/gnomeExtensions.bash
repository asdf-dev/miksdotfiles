#!/usr/bin/env bash

#install gnome extenstions



#Draw On You Screen 
#https://extensions.gnome.org/extension/1683/draw-on-you-screen/
sudo apt install gnome-shell-extension-draw-on-your-screen -y


#Dash to Dock  
#https://extensions.gnome.org/extension/307/dash-to-dock/
sudo apt install gnome-shell-extension-dash-to-dock -y


#No Screen Blank
#https://extensions.gnome.org/extension/2413/no-screen-blank/
sudo apt install gnome-shell-extension-no-screen-blank -y


#Quick Close in Overview
#https://extensions.gnome.org/extension/352/middle-click-to-close-in-overview/
sudo apt install gnome-shell-extension-middle-click-to-close-in-overview -y


#WindowOverlay Icons
#https://extensions.gnome.org/extension/302/windowoverlay-icons/
sudo apt install gnome-shell-extension-windowoverlay-icons -y
